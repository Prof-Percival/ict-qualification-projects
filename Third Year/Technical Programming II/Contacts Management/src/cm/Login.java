    private boolean userFound()
    {
        try
        {
            Connection con = Database.getConnection();
            PreparedStatement ps;
            ResultSet rs;
            
            ps = con.prepareStatement("SELECT Username, Password FROM persons WHERE Username LIKE ? AND Password LIKE ?");
            ps.setString(1, txtUsername.getText());
            ps.setString(2, new String(txtPassword.getPassword()));
            
            rs = ps.executeQuery();
            
            if (rs.next())
            {
                if (txtUsername.getText().equals(rs.getString(1)) && (new String(txtPassword.getPassword()).equals(rs.getString(2))))
                {
                    JOptionPane.showMessageDialog(null, "Login Successfully", "Login", JOptionPane.INFORMATION_MESSAGE);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Incorrect Username / Password", "Login", JOptionPane.ERROR_MESSAGE);
                    return false;
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Incorrect Username / Password", "Login", JOptionPane.ERROR_MESSAGE);
                return false;
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Searching User", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex.getMessage());
            return false;
        }
            
        return true;
    }
    
    

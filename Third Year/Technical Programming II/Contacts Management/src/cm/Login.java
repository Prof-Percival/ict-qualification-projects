    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        //If Fields Are Empty
        if (txtUsername.getText().trim().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Input Username", "Login", JOptionPane.ERROR_MESSAGE);
            txtUsername.requestFocusInWindow();
            txtUsername.setText("");
            return;
        }
        
        if ((new String(txtPassword.getPassword())).trim().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Input Password", "Login", JOptionPane.ERROR_MESSAGE);
            txtPassword.requestFocusInWindow();
            txtPassword.setText("");
            return;
        }

        // Login Details Verification
        if (!userFound())
        {
            return;
        }
        
        // Open Contatcs Form
        Contacts contacts;
        try {
            contacts = new Contacts(txtUsername.getText());
            contacts.setVisible(true);
            contacts.pack();
            contacts.setLocationRelativeTo(null);
            contacts.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            this.dispose();
        } catch (IOException | SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

